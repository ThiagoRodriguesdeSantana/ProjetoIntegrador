    private void preencherCombo() {
        String sql = "select * from titulacao";
        try{
            PreparedStatement pst = conectar.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                cbTitulacao.addItem(rs.getString("desc_titulacao"));
            }
            
        }catch(Exception e){
            
        }
    }
