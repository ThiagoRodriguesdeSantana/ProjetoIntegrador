    private void preencherCombo() {
        String sql = "select * from estado";
        try{
            PreparedStatement pst = conectar.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                cbEstado.addItem(rs.getString("desc_estado"));
            }
            
        }catch(Exception e){
            
        }
    }
